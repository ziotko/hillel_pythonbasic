# 2. Користувач вводить число `X`, яке має два знаки після десяткової точки.
#
#   - Виведіть його дрібну частину.
#   - Виведіть першу цифру після десяткової точки.

x = float(input("Введіть число X:"))

fractional_part = x - int(x)
print("Дрібна частина числа X:", fractional_part)

first_decimal_digit = int((x * 10) % 10)
print("Перша цифра після десяткової точки:", first_decimal_digit)