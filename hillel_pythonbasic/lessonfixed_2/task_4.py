# 4. Дано натуральне число. Потрібно визначити, чи є рік із цим числом високосним. Якщо рік є
#
# високосним, то виведіть `YES`, інакше виведіть `NO`. Нагадаємо, що відповідно до григоріанського календаря, рік є високосним, якщо його номер кратний 4, але не кратний 100, а також якщо він кратний 400.

рік = int(input("Введіть рік: "))

if (рік % 4 == 0 and рік % 100 != 0) or (рік % 400 == 0):
    print("YES")
else:
    print("NO")
